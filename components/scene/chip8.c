static volatile bool is_stop;
static void chip8_task(void *arg)
{

    chip8_t *chip8 = new();
    load_rom(chip8, pong, pong_len);
    ESP_LOGI(TAG, "LOADED ROM TO RAM");

    while (!is_stop)
    {
        for (int i = 0; i < 16; i++)
        {
            keypress(chip8, i, false);
        }
        keypad_key_t key = keypad_scan();

        if (key != KEY_NONE)
        {
            keypress(chip8, keypad_to_chip8(key), true);
        }

        for (int e = 0; e < 10; e++)
        {
            fetch_op(chip8);
            exec_op(chip8);
        }
        update_timers(chip8);

        if (chip8->is_drawing)
        {

            chip8->is_drawing = false;

            display_mux_lock();
            bool *p = chip8->screen;

            for (int y = 0; y < SCREEN_HEIGHT; y++)
            {
                int sy = y * 2;

                for (int x = 0; x < SCREEN_WIDTH; x++)
                {
                    int sx = x * 2;
                    int color = *p++;

                    canvas_draw_pixel_locked(sx, sy, !color);
                    canvas_draw_pixel_locked(sx + 1, sy, !color);
                    canvas_draw_pixel_locked(sx, sy + 1, !color);
                    canvas_draw_pixel_locked(sx + 1, sy + 1, !color);
                }
            }
            display_mux_unlock();
        }

        vTaskDelay(pdMS_TO_TICKS(2));
    }
    delete(chip8);
    vTaskDelete(NULL);
}

void chip8_run()
{
    ESP_LOGI(TAG, "RUN");

    is_stop = false;
    xTaskCreate(chip8_task, "Chip8Task", 10240, NULL, 5, NULL);
}

void chip8_stop()
{
    ESP_LOGI(TAG, "STOP");
    is_stop = true;
}